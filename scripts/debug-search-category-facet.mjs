const base = 'http://localhost:5100/api/front/search?keyword=single';
const response = await fetch(base);
const data = await response.json();
const categoryFacet = data.facets?.find((item) => item.key === 'category');
console.log('status', response.status);
console.log('total', data.meta?.total);
console.log('category facet', categoryFacet);
const filtered = await fetch(`${base}&category=stepper-motor`);
const filteredData = await filtered.json();
console.log('filtered total', filteredData.meta?.total);
